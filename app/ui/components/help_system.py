class HelpButton(ctk.CTkButton):
    """
    Маленька кнопка ❓ для розміщення біля заголовків секцій.
    При натисканні відкриває HelpPopup.

    Використання:
        HelpButton(parent, help_key="ping_monitor")
        HelpButton(parent, port=445)
    """

    def __init__(self, parent, help_key: str = None, port: int = None, **kw):
        self._help_key = help_key
        self._port     = port
        super().__init__(
            parent,
            text="❓",
            width=28, height=28,
            fg_color="transparent",
            hover_color=COLORS["bg_secondary"],
            text_color=COLORS["text_secondary"],
            font=ctk.CTkFont(size=13),
            corner_radius=6,
            command=self._show_help,
            **kw
        )

    def _show_help(self):
        HelpPopup(self, help_key=self._help_key, port=self._port)