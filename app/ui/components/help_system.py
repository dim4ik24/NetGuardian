class HelpPopup(ctk.CTkToplevel):
    """
    Універсальне спливаюче вікно з поясненнями.
    Використання:
        HelpPopup(parent, help_key="ping_monitor")
        HelpPopup(parent, port=445)
    """

    def __init__(self, parent, help_key: str = None, port: int = None):
        super().__init__(parent)

        # Визначаємо контент
        if port is not None:
            content = self._build_port_content(port)
        elif help_key is not None:
            content = SECTION_HELP.get(help_key)
        else:
            return

        if content is None:
            return

        # Налаштування вікна
        self.title(f"NetGuardian — Довідка")
        self.geometry("640x580")
        self.resizable(False, False)
        self.configure(fg_color=COLORS["bg_primary"])
        self.attributes("-topmost", True)

        # Центрування відносно батьківського вікна
        self.after(10, self._center)

        self._build_ui(content)
        self.focus_force()

    def _center(self):
        try:
            pw = self.master.winfo_rootx()
            py = self.master.winfo_rooty()
            mw = self.master.winfo_width()
            mh = self.master.winfo_height()
            x = pw + mw // 2 - 320
            y = py + mh // 2 - 290
            self.geometry(f"640x580+{x}+{y}")
        except Exception:
            pass

    def _build_port_content(self, port: int) -> dict:
        """Конвертує PORT_HELP в формат SECTION_HELP."""
        info = PORT_HELP.get(port, DEFAULT_PORT_HELP)
        fix = info["fix"].replace("{port}", str(port))

        return {
            "title": f"Порт {port}  ·  {info['name']}",
            "color": info["color"],
            "sections": [
                {
                    "heading": f"{info['danger']}  ·  Що це таке?",
                    "text": info["what"]
                },
                {
                    "heading": "⚠️  В чому проблема?",
                    "text": info["problem"]
                },
                {
                    "heading": "🛠️  Що робити? (покрокова інструкція)",
                    "text": fix
                },
            ]
        }

    def _build_ui(self, content: dict):
        accent = content.get("color", COLORS["accent_cyan"])

        # Заголовок
        header = ctk.CTkFrame(self, fg_color=COLORS["bg_secondary"],
                              corner_radius=0)
        header.pack(fill="x")

        ctk.CTkLabel(
            header,
            text=content["title"],
            font=ctk.CTkFont(family="Consolas", size=15, weight="bold"),
            text_color=accent
        ).pack(side="left", padx=24, pady=16)

        ctk.CTkButton(
            header, text="✕  Закрити",
            command=self.destroy,
            width=100, height=32,
            fg_color="transparent",
            border_width=1,
            border_color=COLORS["text_dim"],
            text_color=COLORS["text_secondary"],
            hover_color=COLORS["bg_card"],
            font=ctk.CTkFont(family="Consolas", size=11)
        ).pack(side="right", padx=16)

        # Розділювач
        ctk.CTkFrame(self, height=2, fg_color=accent,
                     corner_radius=0).pack(fill="x")

        # Scrollable content
        scroll = ctk.CTkScrollableFrame(
            self, fg_color=COLORS["bg_primary"], corner_radius=0)
        scroll.pack(fill="both", expand=True, padx=0, pady=0)
        scroll.grid_columnconfigure(0, weight=1)

        for i, section in enumerate(content.get("sections", [])):
            # Блок секції
            card = ctk.CTkFrame(scroll, fg_color=COLORS["bg_card"],
                                corner_radius=10,
                                border_width=1, border_color=COLORS["border"])
            card.grid(row=i, column=0, padx=20, pady=(12 if i == 0 else 6, 6),
                      sticky="ew")
            card.grid_columnconfigure(0, weight=1)

            # Заголовок секції
            ctk.CTkLabel(
                card,
                text=section["heading"],
                font=ctk.CTkFont(family="Consolas", size=12, weight="bold"),
                text_color=accent,
                anchor="w"
            ).grid(row=0, column=0, padx=16, pady=(12, 4), sticky="w")

            # Роздільник
            ctk.CTkFrame(card, height=1,
                         fg_color=COLORS["border"]).grid(
                row=1, column=0, padx=16, pady=0, sticky="ew")

            # Текст
            ctk.CTkLabel(
                card,
                text=section["text"],
                font=ctk.CTkFont(family="Consolas", size=11),
                text_color=COLORS["text_primary"],
                anchor="w",
                justify="left",
                wraplength=560
            ).grid(row=2, column=0, padx=16, pady=(8, 14), sticky="w")

        # Відступ знизу
        ctk.CTkFrame(scroll, height=20,
                     fg_color="transparent").grid(
            row=len(content["sections"]), column=0)


